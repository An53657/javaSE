package Day02.virabledemo;

;
public class vairableDemo01 {
    // 主入口
    public static void main(String[] args) {
        // 定义一个变量
        // 数据类型 变量名 = 值;
        // 数据类型：限定了变量可以存储的数据的类型和范围
        // int(只能存储整数) double(只能存储小数
        // String(只能存储字符串) char(只能存储一个字符) boolean(只能存储true和false)
        // 变量名：是一个标识符，用于唯一标识一个变量，就是存储空间的名字
        // 作用：方便以后的使用和引用
        // 数据值：真正存储在变量中的数据值
        // 作用：给变量赋值，初始化变量
        // 等号：赋值运算符，用于将右侧的值赋给左侧的变量
        int age = 28; // 定义一个整型变量age，并赋值为28
        System.out.println(age); // 打印变量age的值到控制台
    }
}
