package Day02.otherdemo;

public class Demo01 {
    // 字表符使用

    // 这是一个类的定义，main方法是Java程序的入口点
    // main方法是程序的起点，所有Java程序都必须有一个main方法
    public static void main(String[] args) {
        // 打印"Hello, World!"到控制台
        // 输出语句，打印字符串到控制台

        // \t是一个转义字符，表示制表符（Tab），用于在输出中添加水平空格
        // \n是一个转义字符，表示换行符，用于在输出中换行
        System.out.println("name" + '\t' + "agre");
        System.out.println("tme" + '\t' + "28");
    }
}