public class practive01 {
    //这是一个练习，将所学的知识汇总到一起
public static void main(String[] args) {
    // 1.的基本用法
    //定义一个变量，然后进行赋值和输出
    int a = 120; //定义一个整数变量a，并赋值为10
    System.out.println(a); //打印变量a的值
    System.out.println(a); //打印变量a的值
    System.out.println(a); //打印变量a的值
    
    // 2.变量参与运算
    int c = 1; //定义一个整数变量a，并赋值为10000
    int b = 1; //定义一个整数变量b，并赋值为20
    System.out.println(c + b ); //打印c和b的和
    
    // 3.修改变量的值
    c = 100; //将变量c的值修改为100
    System.out.println(c); //打印变量c的值
    System.out.println("------------------"); //打印分隔线
    
    // 4.在一条语句中，可以定义多个变量
    int a1 = 10, b1 = 20, c1 = 30; //定义多个整数变量a1、b1和c1，并赋值
    System.out.println(a1); //打印变量a1的值
    System.out.println(b1); //打印变量b1的值
    System.out.println(c1); //打印变量c1的值

    // 5.变量使用之前必须先定义
}
}


